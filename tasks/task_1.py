numbers = [17, 6.06534, 91, 52, 87, 340, 56]

sum_numbers = sum(numbers)  # находим сумму всех элементов в списке

mx_numbers = max(numbers) #находим максимум из всех элементов списка

mn_numbers = min(numbers) #находим минимум из всех элементов списка

print('Сумма элементов списка:', sum_numbers)  # ввыводи сумму
print('Максимальный элемент списка:', mx_numbers)  # ввыводим максимум
print('Минимальный элемент списка:', mn_numbers)  # ввыводим минимум 
